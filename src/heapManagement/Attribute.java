package heapManagement;

public class Attribute {
	
		private char type;
		private int size;
		public char getType() {
			return type;
		}
		public void setType(char type) {
			this.type = type;
		}
		public int getSize() {
			return size;
		}
		public void setSize(int size) {
			this.size = size;
		}
	

}
